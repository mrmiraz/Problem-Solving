//User function Template for Java

class Solution
{
    public long PowMod(long x, long n, long m)
    {
        long res = 1;
        while(n > 0){
            if((n&1) == 1) res = (res*x)%m;
            x = (x *x)%m;
            n = n>>1;
        }
        return res;
    }
}
